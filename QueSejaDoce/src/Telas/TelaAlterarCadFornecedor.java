    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        
        
        if (listafornecedor.getSelectedRow() != -1){
            
            CadastroFornecedordao caddao = new CadastroFornecedordao();
            
            CadastroFornecedor cad = new CadastroFornecedor();
            
            cad.setNomefornecedor(txtNome.getText());
            cad.setEndereco(txtEndereco.getText());
            cad.setCep(Integer.parseInt(txtcep.getText()));
            cad.setCpnj(txtcnpj.getText());
            cad.setContato1(Integer.parseInt(txtcontato.getText()));
            cad.setContato2(Integer.parseInt(txtcontato2.getText()));
            cad.setData(txtData.getText());
            cad.setId(Integer.parseInt(txtId.getText()));
            
            caddao.alteracaofornecedor(cad);
            
            listarfornecedor();
            
            
        } else {
            
            JOptionPane.showMessageDialog(null, "Selecione um fornecedor para alterar");
            
        }
        
        
        
        
    }//GEN-LAST:event_jButton3ActionPerformed

