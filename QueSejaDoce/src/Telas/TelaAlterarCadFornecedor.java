    public void buscarfornecedor (String buscar){
        
        DefaultTableModel modelo = (DefaultTableModel) listafornecedor.getModel();
        modelo.setRowCount(0);
        CadastroFornecedordao cad = new CadastroFornecedordao();
        
        for(CadastroFornecedor cadastro : cad.buscarfonecedor(buscar)){
            
            modelo.addRow(new Object[]{
                
                cadastro.getId(),
                cadastro.getNomefornecedor()
                       
            });
            
        }       
    }
    
     public void listarfornecedor(){
     
         DefaultTableModel modelo = (DefaultTableModel) listafornecedor.getModel();
         modelo.setRowCount(0);
        CadastroFornecedordao cadastro = new CadastroFornecedordao();
         
         for(CadastroFornecedor cad : cadastro.listarfonecedor()){
             modelo.addRow(new Object[]{
                 
//              
                 cad.getId(),
                 cad.getNomefornecedor(),
              
                 
                 
             });
             
         } 
    }
     
