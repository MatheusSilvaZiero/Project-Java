        Data date = new Data();
        Despesas  desp = new Despesas();
        
        DespesasDao despdao = new DespesasDao();
        
        desp.setFornecedor(txtnomefornecedor.getText());
        desp.setData(txtdata.getText());
        desp.setMes(date.buscarmes());
        desp.setAno(date.buscarano());
